<h4>Opera</h4>
<br>
<h6>Метод 1</h6>
Просто нажать правой кнопкой мыши по поисковому окошку сайта <a href="index.php">тут</a> и выбрать <b>Create Search...</b>. Все необходимые настройки будут заполнены автоматически. Далле нужно добавить клавишу и нажать <b>OK</b>.<br>
<br>

